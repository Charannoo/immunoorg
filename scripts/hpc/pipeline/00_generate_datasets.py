#!/usr/bin/env python3
"""
Stage 0 — Dataset generation
============================

Runs entirely on CPU (no GPU needed). Builds:

1. The 5 raw scenario datasets (curriculum, edge_case, complexity_matrix,
   coevolution, elite_scenario_mix) via DatasetGenerator. ~1700 scenarios.
2. *Real* trajectories by executing the heuristic policy on the elite
   scenario mix — used both as SFT warm-start data and as gold-standard
   demonstrations for GRPO.
3. SFT data in chat-template format (prompt -> completion pairs).
4. GRPO prompt set (prompt-only, completions generated by the model
   during stage 02 training).

All artifacts are saved under DATA_DIR (default: outputs/datasets/) and,
if HF_DATASET_REPO + HF_TOKEN are set, pushed to the Hub as a dataset
repo so the rest of the pipeline (and judges) can pull from one place.

Env overrides:
    IMMUNOORG_DATA_DIR        default outputs/datasets
    IMMUNOORG_NUM_ELITE       default 500   (elite mix size, multiple of 5)
    IMMUNOORG_NUM_TRAJECTORIES default 200  (heuristic rollouts -> SFT data)
    HF_DATASET_REPO           default hirann/immunoorg-grpo-dataset
    HF_TOKEN                  required to push to Hub
"""

from __future__ import annotations

import gzip
import json
import os
import sys
import time
from pathlib import Path

REPO_ROOT = Path(__file__).resolve().parent.parent.parent.parent
sys.path.insert(0, str(REPO_ROOT))

DATA_DIR = Path(os.environ.get("IMMUNOORG_DATA_DIR", str(REPO_ROOT / "outputs" / "datasets")))
NUM_ELITE = int(os.environ.get("IMMUNOORG_NUM_ELITE", "500"))
NUM_TRAJECTORIES = int(os.environ.get("IMMUNOORG_NUM_TRAJECTORIES", "200"))
HF_DATASET_REPO = os.environ.get("HF_DATASET_REPO", "hirann/immunoorg-grpo-dataset")
HF_TOKEN = os.environ.get("HF_TOKEN", "").strip() or None

DATA_DIR.mkdir(parents=True, exist_ok=True)


def banner(msg: str) -> None:
    print()
    print("=" * 70)
    print(f"  {msg}")
    print("=" * 70)


# ── 1. Raw scenario datasets ────────────────────────────────────────────────


def build_raw_datasets() -> dict[str, str]:
    banner(f"[1/4] Generating raw scenario datasets")
    from training.dataset_generator import DatasetConfig, DatasetGenerator

    gen = DatasetGenerator(DatasetConfig(
        dataset_type="all",
        output_dir=str(DATA_DIR / "raw"),
        verbose=True,
        compress_output=True,
    ))
    paths = gen.generate_all_datasets()
    print(f"  generated {len(paths)} dataset files:")
    for k, p in paths.items():
        size_kb = Path(p).stat().st_size / 1024
        print(f"    {k:24s} -> {p}  ({size_kb:.1f} KB)")
    return paths


# ── 2. Heuristic trajectories on elite scenarios ────────────────────────────


def heuristic_action(env, obs):
    """Phase- and hook-aware heuristic — same logic used in
    scripts/generate_training_evidence.py so SFT data and evaluation
    use the same gold-standard policy."""
    from immunoorg.models import (
        ActionType,
        DiagnosticAction,
        ImmunoAction,
        StrategicAction,
        TacticalAction,
    )

    phase = obs.current_phase.value
    nodes = obs.visible_nodes
    compromised = [n for n in nodes if n.compromised and not n.isolated]
    hooks = getattr(env, "_active_scenario_hooks", {}) or {}

    if hooks.get("inject_rag_best_mitigation") and phase in ("detection", "containment") and compromised:
        return ImmunoAction(
            action_type=ActionType.TACTICAL,
            tactical_action=TacticalAction.SNAPSHOT_FORENSICS,
            target=compromised[0].id,
            reasoning="RAG: forensic snapshot before patching the rootkit.",
        )
    if hooks.get("board_uptime_no_isolate") and phase == "containment":
        target = compromised[0].id if compromised else (nodes[0].id if nodes else "")
        return ImmunoAction(
            action_type=ActionType.TACTICAL,
            tactical_action=TacticalAction.DEPLOY_PATCH,
            target=target,
            reasoning="Board uptime directive: patch instead of isolating.",
        )
    if hooks.get("force_denials_on_isolate") and phase in ("containment", "rca", "refactor"):
        return ImmunoAction(
            action_type=ActionType.STRATEGIC,
            strategic_action=StrategicAction.ESTABLISH_DEVSECOPS,
            target="dept-security",
            secondary_target="dept-engineering",
            reasoning="Approver keeps denying; restructure org to remove the bottleneck.",
        )
    if hooks.get("stealthy_initial_attack") and phase == "detection":
        return ImmunoAction(
            action_type=ActionType.DIAGNOSTIC,
            diagnostic_action=DiagnosticAction.VULNERABILITY_SCAN,
            reasoning="Stealth attack: deeper scan before tactical action.",
        )

    if phase == "detection":
        target = compromised[0].id if compromised else (nodes[0].id if nodes else "")
        return ImmunoAction(
            action_type=ActionType.TACTICAL,
            tactical_action=TacticalAction.SCAN_LOGS,
            target=target,
            reasoning="Detection: scan for indicators.",
        )
    if phase == "containment":
        if compromised:
            return ImmunoAction(
                action_type=ActionType.TACTICAL,
                tactical_action=TacticalAction.ISOLATE_NODE,
                target=compromised[0].id,
                reasoning="Isolate the compromised node.",
            )
        return ImmunoAction(
            action_type=ActionType.DIAGNOSTIC,
            diagnostic_action=DiagnosticAction.TIMELINE_RECONSTRUCT,
            reasoning="Reconstruct timeline.",
        )
    if phase == "rca":
        return ImmunoAction(
            action_type=ActionType.DIAGNOSTIC,
            diagnostic_action=DiagnosticAction.IDENTIFY_SILO,
            reasoning="Find the org silo behind the failure.",
        )
    if phase == "refactor":
        return ImmunoAction(
            action_type=ActionType.STRATEGIC,
            strategic_action=StrategicAction.REDUCE_BUREAUCRACY,
            target="dept-management",
            reasoning="Reduce approval latency.",
        )
    return ImmunoAction(
        action_type=ActionType.DIAGNOSTIC,
        diagnostic_action=DiagnosticAction.MEASURE_ORG_LATENCY,
        reasoning="Validate org improvements.",
    )


def collect_heuristic_trajectories():
    banner(f"[2/4] Rolling out heuristic on {NUM_TRAJECTORIES} elite scenarios")
    from immunoorg.environment import ImmunoOrgEnvironment
    from training.dataset_generator import DatasetConfig, DatasetGenerator
    from training.scenario_hooks import (
        apply_scenario_hooks,
        attach_hooks,
        training_step_penalty,
    )

    n = (NUM_TRAJECTORIES // 5) * 5
    gen = DatasetGenerator(DatasetConfig(
        dataset_type="elite",
        output_dir=str(DATA_DIR / "_tmp"),
        verbose=False,
        compress_output=False,
    ))
    scenarios = gen.generate_elite_scenario_mix_dataset(total=n)

    trajectories = []
    t0 = time.time()
    for idx, sc in enumerate(scenarios):
        try:
            env = ImmunoOrgEnvironment(
                difficulty=int(sc["difficulty"]),
                seed=int(sc["seed"]),
            )
            attach_hooks(env, sc.get("hooks") or {})
            obs = env.reset()
            apply_scenario_hooks(env, sc.get("hooks") or {})

            frames = []
            cum_reward = 0.0
            for step in range(min(60, env.state.max_steps)):
                action = heuristic_action(env, obs)
                action_dict = action.model_dump(mode="json")
                obs_dict = obs.model_dump(mode="json")
                obs_next, reward, done = env.step(action)
                shaped = float(reward) + float(training_step_penalty(env, action))
                cum_reward += shaped
                frames.append({
                    "step": step,
                    "observation": obs_dict,
                    "action": action_dict,
                    "reward": shaped,
                    "done": done,
                })
                if done:
                    break
                obs = obs_next

            trajectories.append({
                "scenario_id": sc["scenario_id"],
                "family": sc["family"],
                "difficulty": sc["difficulty"],
                "seed": sc["seed"],
                "hooks": sc.get("hooks") or {},
                "cumulative_reward": cum_reward,
                "num_frames": len(frames),
                "frames": frames,
            })
        except Exception as e:
            print(f"  skipped {sc.get('scenario_id')}: {e}")

        if (idx + 1) % 25 == 0:
            elapsed = time.time() - t0
            rate = (idx + 1) / elapsed
            print(f"  {idx + 1}/{len(scenarios)} done | {rate:.1f}/s | "
                  f"avg reward so far: {sum(t['cumulative_reward'] for t in trajectories) / len(trajectories):+.2f}")

    out = DATA_DIR / "heuristic_trajectories.json.gz"
    with gzip.open(out, "wt", encoding="utf-8") as f:
        json.dump(trajectories, f)
    total_frames = sum(t["num_frames"] for t in trajectories)
    print(f"  saved {len(trajectories)} trajectories ({total_frames} total frames) -> {out}")
    return out, trajectories


# ── 3. SFT data (prompt -> completion) ──────────────────────────────────────


def build_sft_data(trajectories):
    banner(f"[3/4] Building SFT warm-start dataset")
    from immunoorg.agents.defender import (
        format_observation_for_llm,
        get_defender_prompt,
    )

    system = get_defender_prompt()
    samples = []
    for t in trajectories:
        for fr in t["frames"]:
            obs = fr["observation"]
            action = fr["action"]
            obs_text = format_observation_for_llm(obs)
            prompt = (
                f"{system}\n\n"
                f"## Scenario family: {t['family']}\n"
                f"## Current observation\n{obs_text}\n\n"
                f"Respond with a JSON action:"
            )
            completion = json.dumps({
                "action_type": action.get("action_type"),
                "tactical_action": action.get("tactical_action"),
                "strategic_action": action.get("strategic_action"),
                "diagnostic_action": action.get("diagnostic_action"),
                "target": action.get("target", ""),
                "secondary_target": action.get("secondary_target"),
                "parameters": action.get("parameters") or {},
                "reasoning": action.get("reasoning", ""),
            }, indent=2)
            samples.append({
                "prompt": prompt,
                "completion": completion,
                "family": t["family"],
                "difficulty": t["difficulty"],
                "reward": fr["reward"],
            })

    out = DATA_DIR / "sft_warmstart.jsonl.gz"
    with gzip.open(out, "wt", encoding="utf-8") as f:
        for s in samples:
            f.write(json.dumps(s) + "\n")
    print(f"  saved {len(samples)} SFT samples -> {out}")
    return out


# ── 4. GRPO prompt set ──────────────────────────────────────────────────────


def build_grpo_prompts():
    banner(f"[4/4] Building GRPO prompt-only dataset (no completions)")
    from immunoorg.agents.defender import (
        format_observation_for_llm,
        get_defender_prompt,
    )
    from immunoorg.environment import ImmunoOrgEnvironment
    from training.dataset_generator import DatasetConfig, DatasetGenerator
    from training.scenario_hooks import apply_scenario_hooks, attach_hooks

    n = (NUM_ELITE // 5) * 5
    gen = DatasetGenerator(DatasetConfig(
        dataset_type="elite",
        output_dir=str(DATA_DIR / "_tmp"),
        verbose=False,
        compress_output=False,
    ))
    scenarios = gen.generate_elite_scenario_mix_dataset(total=n)

    system = get_defender_prompt()
    samples = []
    for sc in scenarios:
        try:
            env = ImmunoOrgEnvironment(
                difficulty=int(sc["difficulty"]),
                seed=int(sc["seed"]),
            )
            attach_hooks(env, sc.get("hooks") or {})
            obs = env.reset()
            apply_scenario_hooks(env, sc.get("hooks") or {})

            obs_text = format_observation_for_llm(obs.model_dump())
            prompt = (
                f"{system}\n\n"
                f"## Scenario family: {sc['family']}\n"
                f"## Current observation\n{obs_text}\n\n"
                f"Respond with a JSON action:"
            )
            samples.append({
                "prompt": prompt,
                "family": sc["family"],
                "difficulty": sc["difficulty"],
                "scenario_id": sc["scenario_id"],
            })
        except Exception:
            continue

    out = DATA_DIR / "grpo_prompts.jsonl.gz"
    with gzip.open(out, "wt", encoding="utf-8") as f:
        for s in samples:
            f.write(json.dumps(s) + "\n")
    print(f"  saved {len(samples)} GRPO prompts -> {out}")
    return out


# ── HF dataset push ─────────────────────────────────────────────────────────


def push_to_hub() -> None:
    banner(f"Push to HF Hub: {HF_DATASET_REPO}")

    if not HF_TOKEN:
        print("  HF_TOKEN missing; skipping upload. Datasets are still on disk:")
        print(f"    {DATA_DIR}")
        return

    from huggingface_hub import HfApi, create_repo

    api = HfApi(token=HF_TOKEN)
    create_repo(
        repo_id=HF_DATASET_REPO,
        repo_type="dataset",
        token=HF_TOKEN,
        exist_ok=True,
    )

    card = """---
license: apache-2.0
task_categories:
  - reinforcement-learning
tags:
  - immunoorg
  - openenv
  - grpo
  - sft
  - cybersecurity
---

# ImmunoOrg 2.0 — GRPO + SFT training datasets

Generated by the HPC pipeline at
`scripts/hpc/pipeline/00_generate_datasets.py` from the
[ImmunoOrg 2.0](https://huggingface.co/spaces/hirann/immunoorg-v3)
OpenEnv environment.

## Files

| File | What |
| --- | --- |
| `raw/curriculum_dataset.json.gz` | 300 progressive-difficulty scenarios |
| `raw/edge_case_dataset.json.gz` | 400 failure-mode scenarios (12 families) |
| `raw/complexity_matrix_dataset.json.gz` | 300 latin-hypercube combinations |
| `raw/coevolution_dataset.json.gz` | 200 adversary-adapts scenarios |
| `raw/elite_scenario_mix_dataset.json.gz` | 500 conflict-heavy scenarios (20% each of 5 elite families) |
| `heuristic_trajectories.json.gz` | Real heuristic-policy rollouts on the elite mix (used as SFT gold and evaluation baseline) |
| `sft_warmstart.jsonl.gz` | (prompt, completion) pairs for SFT warm-start before GRPO |
| `grpo_prompts.jsonl.gz` | Prompt-only set for GRPO training (model generates its own completions) |
"""
    (DATA_DIR / "README.md").write_text(card, encoding="utf-8")

    print("  uploading dataset folder...")
    api.upload_folder(
        folder_path=str(DATA_DIR),
        repo_id=HF_DATASET_REPO,
        repo_type="dataset",
        commit_message="HPC pipeline stage 0 — dataset build",
        ignore_patterns=["_tmp/*", "_tmp/**"],
    )
    print(f"  pushed -> https://huggingface.co/datasets/{HF_DATASET_REPO}")


# ── Main ────────────────────────────────────────────────────────────────────


def main() -> int:
    banner("ImmunoOrg 2.0 — Stage 0: Dataset generation")
    print(f"  data_dir         : {DATA_DIR}")
    print(f"  num_elite        : {NUM_ELITE}")
    print(f"  num_trajectories : {NUM_TRAJECTORIES}")
    print(f"  push_repo        : {HF_DATASET_REPO}")
    print(f"  HF_TOKEN set     : {bool(HF_TOKEN)}")

    build_raw_datasets()
    _, trajectories = collect_heuristic_trajectories()
    build_sft_data(trajectories)
    build_grpo_prompts()
    push_to_hub()

    banner("DONE — Stage 0")
    return 0


if __name__ == "__main__":
    sys.exit(main())
